package com.panli.interview.合并两个有序链表;

/**
 * @author lipan
 * @date 2021-03-29
 */
public class ListNode {

    public int val;
    public ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
